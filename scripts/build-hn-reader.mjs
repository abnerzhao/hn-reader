import { readdir, readFile, writeFile } from 'node:fs/promises';
import { join, relative, resolve } from 'node:path';

const root = resolve(import.meta.dirname, '..');
const articlesDir = join(root, 'content/articles');
const outputFile = join(articlesDir, 'articles.js');
const requiredLevels = ['easy', 'medium', 'hard'];

function parseFrontmatter(markdown, file) {
  const match = markdown.match(/^---\n([\s\S]*?)\n---\n([\s\S]*)$/);
  if (!match) throw new Error(`${file}: missing frontmatter`);
  const metadata = Object.fromEntries(match[1].split('\n').filter(Boolean).map(line => {
    const separator = line.indexOf(':');
    if (separator === -1) throw new Error(`${file}: invalid frontmatter line "${line}"`);
    return [line.slice(0, separator).trim(), line.slice(separator + 1).trim()];
  }));
  return { metadata, body: match[2].trim() };
}

function parseArticle(markdown, file) {
  const { metadata, body } = parseFrontmatter(markdown, file);
  for (const key of ['title', 'date', 'source_name', 'source_url']) {
    if (!metadata[key]) throw new Error(`${file}: missing ${key}`);
  }
  if (!/^\d{4}-\d{2}-\d{2}$/.test(metadata.date)) throw new Error(`${file}: date must use YYYY-MM-DD`);

  const sections = body.split(/^## (easy|medium|hard)\s*$/m);
  const editions = {};
  for (let index = 1; index < sections.length; index += 2) {
    const level = sections[index];
    const paragraphs = sections[index + 1].trim().split(/\n{2,}/).map(paragraph => paragraph.replace(/\n/g, ' ').trim()).filter(Boolean);
    editions[level] = { paragraphs };
  }
  for (const level of requiredLevels) {
    if (!editions[level]?.paragraphs.length) throw new Error(`${file}: missing ## ${level} content`);
  }

  const id = relative(articlesDir, file).replace(/\.md$/, '').replace(/^\d{4}-\d{2}-\d{2}-/, '');
  return {
    id,
    date: metadata.date,
    title: metadata.title,
    sourceName: metadata.source_name,
    sourceUrl: metadata.source_url,
    editions
  };
}

const entries = await readdir(articlesDir, { withFileTypes: true });
const articles = await Promise.all(entries
  .filter(entry => entry.isFile() && entry.name.endsWith('.md'))
  .map(async entry => {
    const file = join(articlesDir, entry.name);
    return parseArticle(await readFile(file, 'utf8'), file);
  }));

articles.sort((a, b) => b.date.localeCompare(a.date));
const data = Object.fromEntries(articles.map(({ id, ...article }) => [id, article]));
await writeFile(outputFile, `// Generated by scripts/build-hn-reader.mjs. Do not edit manually.\nwindow.HN_READER_ARTICLES = ${JSON.stringify(data, null, 2)};\n`);
console.log(`Built ${articles.length} article${articles.length === 1 ? '' : 's'} → ${relative(root, outputFile)}`);
